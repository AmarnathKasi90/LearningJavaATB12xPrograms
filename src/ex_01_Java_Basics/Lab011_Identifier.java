package ex_01_Java_Basics;

public class Lab011_Identifier {
    public static void main(String[] args) {
        System.out.println("Hi");
        System.out.println("Below are Identifiers");

        /**
         * ex_01_Java_Basics      → package name
         * Lab011_Identifier      → class name
         * main                   → method name
         * String                 → class type (from java.lang)
         * args                   → parameter name
         * System                 → built-in Java class
         * out                    → static field of System
         * println                → method of out
         */

    }
}